#include <unistd.h>

void	ft_is_negative(int n)
{	
	char	a;
	char	p;

	a = 'N';
	p = 'P';
	if (n < 0)
	{
		write (1, &a, 1);
	}
	else if (n >= 0)
	{
		write (1, &p, 1);
	}
}
